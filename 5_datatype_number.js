// javascript uses the number datatype to represent both integer and floatnumber

// js uses number to represent integer 

var intNumber = 10020;
console.log('The integer number is: ',intNumber );
console.log('The type of intNumber is : ',typeof intNumber);
// output:
// $ node 5_datatype_number.js
// The integer number is:  10020


// js uses the number data type to to represent floating point 

var floatNumber = 1000.2012161651;
console.log('The floating point number is',floatNumber);
console.log('The type of floating point number is: ',typeof floatNumber);

// output:
// The floating point number is 1000.2012161651
// The type of floating point number is:  number


// javascript convert float number to integer if it appears as whole number 

var fNumber = 200.000;
console.log('The float number is converted to integer',fNumber);
// output:
// The float number is converted to integer 200


// To get range of number data type.

console.log('The max value of number data type is ',Number.MAX_VALUE);
console.log('The min value of number data type is ',Number.MIN_VALUE);

// output:
// The max value of number data type is  1.7976931348623157e+308
// The min value of number data type is  5e-324


